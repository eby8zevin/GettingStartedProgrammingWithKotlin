package berkenalandenganCoroutines

fun main(){

    println("concurrency is not parallelism")

    println("Example: concurrency")
    println("Barista = Cappuccino, 4 Latte, Espresso, 2 Green Tea, Chocolate, 3 White Coffee")
    println()
    println("Example: parallelism")
    println("Barista 1 = Cappuccino, 4 Latte, Espresso")
    println("Barista 2 = 2 Green Tea, Chocolate, 3 White Coffee")
    println()
    println("That is, parallelism can lead to concurrency, but concurrency can occur without parallelism.")
}