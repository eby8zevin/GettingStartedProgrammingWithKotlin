fun main() {
    // var = public & val = private
    // var identifier: Type = initialization
    var company: String = "Dicoding"
    println(company)
    company = "Dicoding Academy"
    println(company)

    val firsWord = "Dicoding "
    val lastWord = "Academy"
    println(firsWord + lastWord)

    val valueA: Int = 10
    val valueB = 20
    print(valueA + valueB)
}